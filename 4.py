x = int(input("Введите x коордиту первого ферзя: "))
y = int(input("Введите y коордиту первого ферзя: "))
x1 = int(input("Введите x коордиту второго ферзя: "))
y1 = int(input("Введите y коордиту второго ферзя: "))

if x == x1 or y == y1 or (abs(x - x1) == abs(y - y1) ):
    print("совпадают")
else:
    print("несовпадают")
    
